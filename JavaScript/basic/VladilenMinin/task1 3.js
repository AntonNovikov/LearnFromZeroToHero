let productName = "Cheese" // название товара.
let productPrice =  4;// стоимость.
let productQuantity = 8 // количество купленного товара.
let resultCost =  productPrice* productQuantity// итоговая стоимость (высчитать динамически, с учетом цены и количества товара).

console.log(`Вы купили ${productName} (${productQuantity} шт.) по ${productPrice} ₽. Всего было потрачено: ${resultCost} ₽`)